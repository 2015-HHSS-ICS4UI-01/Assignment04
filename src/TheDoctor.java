public class TheDoctor {
    private int x;
    private int y;
    
    public TheDoctor(int startX, int startY)
    {
        x = startX;
        y = startY;
    }
    
    public int getX()
    {
        return x;
    }
    
    public int getY()
    {
        return y;
    }
}
